/**
 * Helper function to generate content using OpenAI as fallback
 * @param prompt - The prompt to send to OpenAI
 * @param maxTokens - Maximum tokens for the response
 * @returns Generated content or null if failed
 */
async function generateContentWithOpenAI(prompt: string, maxTokens: number = 1000) {
  try {
    // Check if OpenAI is configured
    if (!process.env.OPENAI_API_KEY) {
      console.log('OpenAI API key not configured, skipping OpenAI fallback');
      return null;
    }

    const response = await openaiClient.chat.completions.create({
      model: process.env.OPENAI_MODEL || 'gpt-4-turbo',
      messages: [
        {
          role: 'user',
          content: prompt,
        },
      ],
      max_tokens: maxTokens,
      temperature: 0.7,
    });

    const generatedText = response.choices[0]?.message?.content?.trim();
    if (!generatedText) {
      throw new Error('No content generated by OpenAI');
    }

    // Extract JSON from the response with better error handling
    const jsonMatch = generatedText.match(/\{[\s\S]*\}/);
    if (!jsonMatch) {
      throw new Error('Could not extract JSON from OpenAI response');
    }

    let generatedContent;
    try {
      generatedContent = JSON.parse(jsonMatch[0]);
    } catch (parseError) {
      console.error('JSON parse error in OpenAI fallback:', parseError);
      console.error('Raw JSON string:', jsonMatch[0]);
      
      // Try to clean up common JSON issues
      let cleanedJson = jsonMatch[0]
        .replace(/,(\s*[}\]])/g, '$1') // Remove trailing commas
        .replace(/([{,]\s*)(\w+):/g, '$1"$2":') // Add quotes to unquoted keys
        .replace(/:\s*'([^']*)'/g, ': "$1"') // Replace single quotes with double quotes
        .replace(/\n/g, ' ') // Remove newlines
        .replace(/\s+/g, ' '); // Normalize whitespace
      
      try {
        generatedContent = JSON.parse(cleanedJson);
        console.log('✅ Successfully parsed cleaned JSON for OpenAI fallback');
      } catch (secondParseError) {
        console.error('Failed to parse even cleaned JSON:', secondParseError);
        throw new Error('Could not parse JSON from OpenAI response');
      }
    }
    console.log('✅ Generated content using OpenAI fallback');
    return generatedContent;

  } catch (error) {
    console.error('Error generating content with OpenAI:', error);
    return null;
  }
}

