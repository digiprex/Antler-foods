/**
 * Fetch multiple gallery-ready media images for the restaurant
 */
async function getRestaurantGalleryImages(
  restaurantId: string,
  limit: number = 12,
) {
  let galleryPromise = restaurantGalleryImageCache.get(restaurantId);
  if (!galleryPromise) {
    galleryPromise = (async () => {
      const query = `
        query GetRestaurantGalleryMedia($restaurant_id: uuid!, $limit: Int!) {
          medias(
            where: {
              restaurant_id: { _eq: $restaurant_id },
              _and: [
                {
                  _or: [
                    { is_deleted: { _eq: false } },
                    { is_deleted: { _is_null: true } }
                  ]
                },
                {
                  _or: [
                    { is_hidden: { _eq: false } },
                    { is_hidden: { _is_null: true } }
                  ]
                },
                {
                  _or: [
                    { type: { _eq: "image" } },
                    { type: { _ilike: "image/%" } },
                    { type: { _ilike: "image%" } },
                    { type: { _eq: "gallery" } }
                  ]
                }
              ]
            },
            order_by: { created_at: asc },
            limit: $limit
          ) {
            file_id
            alt_text
          }
        }
      `;

      const data = await adminGraphqlRequest<{
        medias: Array<{
          file_id: string;
          alt_text?: string | null;
        }>;
      }>(query, {
        restaurant_id: restaurantId,
        limit: GALLERY_MEDIA_CACHE_LIMIT,
      });

      if (!data.medias || data.medias.length === 0) {
        return [];
      }

      return data.medias.map((media, index) => {
        const alt = media.alt_text?.trim() || `Gallery image ${index + 1}`;
        return {
          id: media.file_id,
          url: `/api/image-proxy?fileId=${media.file_id}`,
          alt,
          title: alt,
          order: index,
        };
      });
    })();
    restaurantGalleryImageCache.set(restaurantId, galleryPromise);
  }

  try {
    const allImages = await galleryPromise;
    if (limit <= 0 || allImages.length <= limit) {
      return allImages;
    }
    return allImages.slice(0, limit);
  } catch (error) {
    restaurantGalleryImageCache.delete(restaurantId);
    throw error;
  }
}

function getRandomMediaSelection<T>(items: T[], count: number): T[] {
  if (!Array.isArray(items) || items.length === 0 || count <= 0) {
    return [];
  }

  const shuffled = [...items];
  for (let index = shuffled.length - 1; index > 0; index -= 1) {
    const randomIndex = Math.floor(Math.random() * (index + 1));
    [shuffled[index], shuffled[randomIndex]] = [shuffled[randomIndex], shuffled[index]];
  }

  return shuffled.slice(0, Math.min(count, shuffled.length));
}

async function enrichCustomSectionMediaConfig(
  restaurantId: string,
  baseConfig: Record<string, any>,
  fallbackLayoutId?: string,
) {
  const rawLayoutId = (
    baseConfig?.layout ||
    fallbackLayoutId ||
    'layout-1'
  ) as CustomSectionLayout;
  const layoutDefinition = getCustomSectionLayoutDefinition(rawLayoutId);
  const layoutId = layoutDefinition.value;
  const imageSlots = layoutDefinition.mediaSlots.filter(
    (slot) =>
      slot === 'image' ||
      slot === 'secondaryImage' ||
      slot === 'fallbackImage' ||
      slot === 'backgroundImage',
  );
  const seededItems = layoutDefinition.supportsItems
    ? createLayoutItems(layoutId, baseConfig?.items as any) || []
    : [];
  const imagePoolLimit = Math.max(
    imageSlots.length,
    seededItems.length,
    layoutDefinition.defaultItemCount || 0,
    12,
  );
  const mediaImagePool = await getRestaurantGalleryImages(
    restaurantId,
    imagePoolLimit,
  );

  const slotImages = imageSlots.length > 0
    ? getRandomMediaSelection(mediaImagePool, imageSlots.length)
    : [];

  const customSectionMediaConfig: Record<string, any> = {};
  imageSlots.forEach((slot, index) => {
    const selected = slotImages[index];
    if (!selected) {
      return;
    }

    if (slot === 'backgroundImage') {
      if (!baseConfig?.backgroundImage) {
        customSectionMediaConfig.backgroundImage = selected.url;
      }
      return;
    }

    const hasExisting = Boolean(baseConfig?.[slot]?.url);
    if (!hasExisting) {
      customSectionMediaConfig[slot] = {
        url: selected.url,
        alt: selected.alt || 'Section image',
      };
    }
  });

  const itemImages = seededItems.length > 0
    ? getRandomMediaSelection(mediaImagePool, seededItems.length)
    : [];
  const customSectionItems = seededItems.length > 0
    ? seededItems.map((item: any, index: number) => {
        if (item?.image?.url) {
          return item;
        }
        const selected = itemImages.length > 0
          ? itemImages[index % itemImages.length]
          : null;
        if (!selected) {
          return item;
        }
        return {
          ...item,
          image: {
            url: selected.url,
            alt: selected.alt || item?.title || `Item ${index + 1}`,
          },
        };
      })
    : undefined;

  return {
    ...(baseConfig || {}),
    layout: layoutId,
    ...customSectionMediaConfig,
    ...(customSectionItems ? { items: customSectionItems } : {}),
  };
}

